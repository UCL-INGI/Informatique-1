package student;
import static org.junit.Assert.*;
import org.junit.runner.JUnitCore;
import org.junit.runner.Result;
import org.junit.Test;
import java.util.Random;
import org.junit.runner.notification.Failure;

public class M1Q9{

	private static String str = "Le code est incorrect : ";
	// Code verificateur
	@Test
	public void testExp(){
		int res;
		int expected;
		try{
			// Iterations pour verifier le code (plusieurs a et n differents)
			for(int a = 0 ; a < 100 ; a++){
				for(int n = 0 ; n < 100 && Math.pow(a,n) < Integer.MAX_VALUE ; n++){
					res = M1Q9Stu.pow(a, n);
					expected = (int) Math.pow(a,n);
					assertEquals(str + a +"^" + n + " retourne "+ res + " mais devrait retourner " + expected, expected, res);
				}
			}
		}catch (ArithmeticException e){
			fail(str + "il est interdit de diviser par zéro.");
			e.printStackTrace();
		}catch(ClassCastException e){
			fail(str + "Attention, certaines variables ont été mal castées	!");
			e.printStackTrace();
		}catch(StringIndexOutOfBoundsException e){
			e.printStackTrace();
			fail(str + "Attention, vous tentez de lire en dehors des limites d'un String ! (StringIndexOutOfBoundsException)");
			e.printStackTrace();
		}catch(ArrayIndexOutOfBoundsException e){
			e.printStackTrace();
			fail(str + "Attention, vous tentez de lire en dehors des limites d'un tableau ! (ArrayIndexOutOfBoundsException)");
			e.printStackTrace();
		}catch(NullPointerException e){
			fail(str + "Attention, vous faites une opération sur un objet qui vaut null ! Veillez à bien gérer ce cas.");
			e.printStackTrace();
		}catch(Exception e){
			fail(str + "\n" + e.getMessage());
			e.printStackTrace();
		}
	}
	
	
	public static void main(String[] args) {
		Result result = JUnitCore.runClasses(M1Q9.class);
		for (Failure failure: result.getFailures()) {
			System.err.println(failure.toString());
		}
		if (result.wasSuccessful()) {
			System.out.println("Tous les tests se sont passés sans encombre");
			System.exit(127);
		}
	}
}
